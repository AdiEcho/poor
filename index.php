<script>
var $ = mdui.JQ;

function go() {
    var x = mdui.snackbar({
        message: '处理中',
        position: 'right-top'
    });
    $.ajax({
        method: 'POST',
        url: './api.php',
        timeout: 10000,
        data: {
            'money': $('#money').val(),
            'comment': $('#comment').val()
        },
        success: function(data) {
            x.close();
            data = eval('(' + data + ')');
            if (data[0] == '200') {
                    mdui.snackbar({
                        message: '处理成功,跳转中...',
                        position: 'right-top'
                    });
                window.location.href = 'pay.php';
            } else {
                mdui.snackbar({
                    message: data[1],
                    position: 'right-top'
                });
            }
        },
        complete: function(xhr, status) {
            if (status == 'timeout') {
                x.close();
                mdui.snackbar({
                    message: '请求超时',
                    position: 'right-top'
                });
            }
        }
    });
}
</script>
